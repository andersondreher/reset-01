public class Calculadora {

    int somar(int valorUm, int valorDois) {
        return valorUm + valorDois;
    }

    int subtracao(int valorUm, int valorDois) {
        return valorUm - valorDois;
    }

    int multiplicacao(int valorUm, int valorDois) {
        return valorUm * valorDois;
    }

    double divisao(double valorUm, double valorDois) {
        return valorUm / valorDois;
    }
}
